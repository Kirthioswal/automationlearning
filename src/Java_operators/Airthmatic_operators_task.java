package Java_operators;

public class Airthmatic_operators_task {
    public static void main(String[] args) {
        int a=20;
        int b=5;
        float c =4.5f;
        System.out.println(a+c); //this is addition operator
        System.out.println(a-b); //this is a substraction operator
        System.out.println(a*c); //this is a multiplication operator
        System.out.println(a/c); //this is a division operator
        System.out.println(a%b); /*this is a modulus operator (remainder) --
        any number divided by 2 and remainder is 0 then that number is even else odd */
        System.out.println("a+b"); /* this will result in string format i,e a+b not sum of a+b
        because we have placed it in "" quotes */
    }
}
